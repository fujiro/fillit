#include "libft.h"

char	**ft_letter(char **piece, int c)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (piece[j][i])
	{
		if (piece[j][i] == '#')
			piece[j][i] = c;
		i++;
		if (i == 4 && j < 3)
		{
			i = 0;
			j++;
		}
	}
	return (piece);
}
