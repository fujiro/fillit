int		ft_sqrt(int nb)
{
	int		sqrt;

	if (nb > 800000000)
		sqrt = 28200;
	else
		sqrt = 2;
	while (sqrt < 46340)
	{
		if ((sqrt * sqrt) >= nb)
			return (sqrt);
		sqrt++;
	}
	return (0);
}
